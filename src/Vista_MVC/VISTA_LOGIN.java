    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String busqueda_usuario = registro_vendedor.buscarUsuarioRegistrado(txtusuario.getText(),txtcontra.getText());
        if(txtusuario.getText().equals("root")&& txtcontra.getText().equals("root")){
            JOptionPane.showMessageDialog(this,"Binevenido iniciaste sesion como Administrador");
            VISTA_FORMULARIO f = new VISTA_FORMULARIO();
            f.lblnombre.setText("Administrador");
            f.setVisible(true);
            this.dispose();
        }else if (busqueda_usuario.equals("usuario encontrado")){
            String  busqueda_nombre = registro_vendedor.BuscarNombre(txtusuario.getText());
            JOptionPane.showMessageDialog(this,"Bienvenido (a) " + busqueda_nombre) ;
            VISTA_FORMULARIO f = new VISTA_FORMULARIO();
            f.setVisible(true);
            f.lblnombre.setText(busqueda_nombre);
            
            this.dispose();
            
        }else 
            JOptionPane.showMessageDialog(this,"Usuario NO registado, porfavor darse de alta");
        
        
        
        
    }//GEN-LAST:event_jButton2ActionPerformed
   
            
            
            
            
